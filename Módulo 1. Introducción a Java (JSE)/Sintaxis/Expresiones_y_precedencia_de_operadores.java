package Tema1.Sintaxis;

public class Expresiones_y_precedencia_de_operadores {
    public static void main(String[] args) {
        System.out.println("Hola mundo"); //"Hola mundo" expresión con un sólo operando
        int i = 0; i++; //Un sólo operando y un operador, i se incrementa en una unidad (i=i+1)
        i += 5; //Dos operadores y un operando (i= i + 5)
        
        long l=100;
        byte b;
        b=(byte)l;
    }
}
